import java.util.Scanner;

public class Main {
    public static void main(String[] args){
        //scanner 변수 선언, 초기화
        Scanner sc = new Scanner(System.in);
        
        //첫번째 수 A
        int a = sc.nextInt();
        //두번째 수 B
        int b = sc.nextInt();
        
        System.out.println(a + b);
    }//main
}//end class