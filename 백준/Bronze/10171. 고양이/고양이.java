 public class Main {
     
     public static void main(String[] args) {
         
         // 백슬래쉬(\) 문자는 이스케이프 문자이므로, 그 자체를 표현할 수 없다.
         // 이때 백슬래쉬(\) 두번 사용해서 해결한다!
         System.out.println("\\    /\\");   
         System.out.println(" )  ( ')");
         System.out.println("(  /  )");
         System.out.println(" \\(__)|");
         
     } // main
     
 } // end class