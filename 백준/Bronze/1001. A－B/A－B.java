import java.util.Scanner;

public class Main{
    public static void main(String[] args){
        //scanner 변수 선언, 초기화
        Scanner sc = new Scanner(System.in);
        //a입력받기
        int a = sc.nextInt();
        //b입력받기
        int b = sc.nextInt();
        //a-b출력
        System.out.println(a-b);
    }//main
}//end class